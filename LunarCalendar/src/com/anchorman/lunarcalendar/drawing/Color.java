package com.anchorman.lunarcalendar.drawing;

/**
 *
 * @author NamNT2
 */
public class Color {

    int red;
    int green;
    int blue;

    public int getRed() {
        return red;
    }

    public int getBlue() {
        return blue;
    }

    public int getGreen() {
        return green;
    }

    public Color(int r, int g, int b) {
        red = r;
        green = g;
        blue = b;
    }
}
